//GET ALL USER
router.get("/", verifyTokenAndAdmin, async (req, res) => {
  //if there are new users query return new users
  const query = req.query.new;
  try {
    const users = query
      ? await User.find().sort({ _id: -1 }).limit(5)
      : await User.find();

    res.status(200).json(users);
  } catch (err) {
    res.status(500).json(err);
  }
});

//GET USER STATS

router.get("/stats", verifyTokenAndAdmin, async (req, res) => {
  //this route will just send no. of users registered month wise
  const date = new Date(); //current date
  const lastYear = new Date(date.setFullYear(date.getFullYear() - 1)); // returns the exactly before one year

  try {
    const data = await User.aggregate([
      { $match: { createdAt: { $gte: lastYear } } }, //condition ---users created between lastyear and till date
      {
        $project: {
          month: { $month: "$createdAt" },
        },
      },
      {
        $group: {
          _id: "$month",
          total: { $sum: 1 },
        },
      },
    ]);
    res.status(200).json(data);
  } catch (err) {
    res.status(500).json(err);
  }
});

module.exports = router;
